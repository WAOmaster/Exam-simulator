const mammoth = require('mammoth');
const fs = require('fs');
const path = require('path');

async function parseQuestions() {
  try {
    const docxPath = path.join(__dirname, '../public/questions.docx');
    const result = await mammoth.extractRawText({ path: docxPath });
    const text = result.value;

    // Split by "Topic X Question Y" pattern
    const questionBlocks = text.split(/Topic \d+ Question \d+/);

    const questions = [];
    let questionId = 1;

    for (let block of questionBlocks) {
      if (!block.trim()) continue;

      // Split content by "Choices:"
      const parts = block.split('Choices:');
      if (parts.length < 2) continue;

      const questionText = parts[0].trim();
      if (!questionText || questionText.length < 10) continue;

      // Extract options and answer from the second part
      const optionsAndAnswer = parts[1];

      // Split by "Correct Answer:"
      const answerSplit = optionsAndAnswer.split(/Correct Answer:\s*/);
      const optionsText = answerSplit[0];
      const correctAnswer = answerSplit[1] ? answerSplit[1].trim().charAt(0) : null;

      // Parse options - split by single uppercase letters followed by period
      const options = [];
      // First, remove any "Correct Answer" from optionsText
      const cleanOptionsText = optionsText.split('Correct Answer')[0];

      // Split by pattern: capital letter followed by period at the start of option
      // Use a more specific pattern that looks for letter + period + space
      const optionParts = cleanOptionsText.split(/(?=[A-E]\.\s)/);

      for (const part of optionParts) {
        const trimmed = part.trim();
        if (!trimmed) continue;

        // Extract option letter and text
        const match = trimmed.match(/^([A-E])\.\s*(.+)$/s);
        if (match) {
          const optionId = match[1];
          const optionText = match[2].trim();

          if (optionText) {
            options.push({
              id: optionId,
              text: optionText
            });
          }
        }
      }

      // Only add if we have valid data
      if (questionText && options.length >= 2 && correctAnswer) {
        questions.push({
          id: questionId++,
          question: questionText,
          options: options,
          correctAnswer: correctAnswer,
          explanation: '', // Will be generated by AI
          category: 'OCI 1Z0-1151-25',
          difficulty: 'medium'
        });
      }
    }

    // Save to JSON
    const outputPath = path.join(__dirname, '../data/questions.json');
    fs.writeFileSync(outputPath, JSON.stringify(questions, null, 2));

    console.log(`✅ Successfully parsed ${questions.length} questions`);
    console.log(`📁 Saved to: ${outputPath}`);

    // Show first question as sample
    if (questions.length > 0) {
      console.log('\n📋 Sample question:');
      console.log(JSON.stringify(questions[0], null, 2));
    }

  } catch (error) {
    console.error('❌ Error parsing questions:', error);
  }
}

parseQuestions();
